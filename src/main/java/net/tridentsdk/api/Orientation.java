package net.tridentsdk.api;

/**
 * The 16 different orientations available in MC, plus up and down
 */
public enum Orientation {
    NORTH,
    SOUTH,
    EAST,
    WEST,

    NORTH_WEST,
    SOUTH_WEST,
    NORTH_EAST,
    SOUTH_EAST,

    NORTH_NORTH_WEST,
    NORTH_WEST_WEST,
    SOUTH_SOUTH_WEST,
    SOUTH_WEST_WEST,

    NORTH_NORTH_EAST,
    NORTH_EAST_EAST,
    SOUTH_SOUTH_EAST,
    SOUTH_EAST_EAST,

    UP,
    DOWN
}
